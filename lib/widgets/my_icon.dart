class CustomIcon extends StatelessWidget {
  final String iconName;
  final double size;
  final Color color;

  const CustomIcon({
    Key? key,
    required this.iconName,
    this.size = 24.0,
    this.color = MyColor.base5,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Image.asset(
      'assets/icons/$iconName.png',
      width: size,
      height: size,
      color: color,
      fit: BoxFit.contain,
    );
  }
}
